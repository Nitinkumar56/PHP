  <h2>Dental care should begin at an early age</h2>

<p style="font-size:17px;">We want parents to be aware that an early start in regular dental care is an important step on the road to total health.</p><br />
  
 <p style="font-size:17px;">Doctors recommend Children begin routine dental care by age 1, so any problems may be detected and treated early or avoided completely.</p>
 
 <p style="font-size:17px;"><b>Baby teeth</b></p>

 <p style="font-size:17px;">Though they are not permanent, primary (baby) teeth are important to your child's future dental health because they:</p>

 
 <p style="font-size:17px;">*Are necessary for proper chewing and eating.</p>
 <p style="font-size:17px;">*Provide space for the permanent teeth and guide them into position.</p>
   <p style="font-size:17px;">  *Permit normal development of the jaw and facial muscles.</p>
   <p style="font-size:17px;">  *Assist in the development of speech.</p>
   <p style="font-size:17px;">  *Add to an attractive appearance.</p>

  <p style="font-size:17px;"><b>Dental health tips</b></p>

  
  <p style="font-size:17px;">Good preventive dental care can never start too early.</p>


 <p style="font-size:17px;"><b>The following list provides some basic dental health tips all parents should know:</b></p>

<nav style="font-size:17px;">
 <p><li>It is recommended you first take your baby to the dentist at 9 to 12 months or when the first tooth appears. Regular visits every 6 months are suggested thereafter. </li></p>
 
 <p><li>Do not delay having your child's cavities treated. Baby teeth need to be treated early because of their importance to the development of adult teeth.</li></p>

<p><li>Brush your child's teeth when the first tooth appears. Use a soft bristle and warm water. You do not need toothpaste at this young age. </li></p>
   
   <p><li> Flossing should begin when the teeth touch each other, usually at age 3 or 4. Flossing can be hard to learn and children may need help with flossing until their early teen years. </li></p>

   
   <p><li>Protect your child from mouth injuries. As your baby learns to climb, watch him or her closely.</li></p></nav>

   
 <p style="font-size:17px;"><b> Regular visits to the dentist recommended</b></p>

 

 <p style="font-size:17px;">  Early and regular visits to the dentist can help guide the development of your child's teeth and prevent future problems.</p><br />

<p style="font-size:17px;">Early visits also will make your child more comfortable about visiting a dentist as he or she grows. Preventive care and early screening can help children avoid costly, major dental problems as they grow.
</p><br />

 <p style="font-size:17px;"> Your child's first set of teeth ("baby" teeth) need as much care and attention as the permanent teeth. In fact, the American Academy of Pediatric Dentists recommends you first take your baby to the dentist at 9 to 12 months of age, or when the first tooth erupts. Regular visits every six months are suggested thereafter.</p><br />


<p style="font-size:17px;">Your child's first oral exam can reveal current as well as potential problems. This early visit also is a good chance for you to establish a rapport with your child's dentist and gather a few tips on dental care.</p>

   
  <p style="font-size:17px;"><b>These include:</b></p>
 
  
  <p style="font-size:17px;">Do not give a baby a bottle to suck overnight. This constant intake of sugars from juice or milk can rapidly decay a baby's teeth. Even chronic breast feeding can lead to decay. Both chronic bottle sucking and nursing can lead to a syndrome in which several cavities form in a young child.</p><br />


  <p  style="font-size:17px;">Do not delay having a young child's cavities treated. Baby teeth need to be treated early, as they are important to the development of the adult teeth.</p><br />


<p style="font-size:17px;">Check for constant thumb or bottle sucking. Both can lead to abnormal growth and development of your child's teeth.</p><br />


<p style="font-size:17px;"> Brush your child's teeth when the first tooth appears. Use a soft bristle brush and warm water. You do not need to use toothpaste at this young age. (It is the action of brushing, not the toothpaste, that keeps teeth clean.) Brushing early establishes a good daily habit for your child. At 1 to 2 years of age, you may use a tiny amount of toothpaste if you think your child is not likely to swallow it. If your child does not like the toothpaste, do not use it.</p><br />

  
  <p style="font-size:17px">Flossing should begin when the teeth touch each other, usually at age 3 to 4. Flossing can be hard to learn and children may need help with flossing until their early teen years.</p><br />

  <p  style="font-size:17px">Protect your child from mouth injuries. As your baby learns to climb, watch the baby closely. Never leave a toddler unattended in a highchair or shopping cart. Refrain from using an infant walker, as many children injure their mouths and other body parts from falls in walkers. Watch your child around stairways. As your baby learns to walk, cover sharp corners on furniture and check for items (rugs, toys) that could cause your child to trip.</p><br />


<p style="font-size:17px;">As your child grows, a variety of preventive measures are available from the dentist to ensure a healthy smile. The dentist can:</p><br />

<p style="font-size:17px;">Watch and treat early tooth eruption problems. At each visit, a pediatric dentist can assess the growth of your child's face to see if teeth are erupting properly.
</p><br />

<p style="font-size:17px;">Apply sealants to prevent cavities. Sealants are bonded to teeth that have grooves or pits - mainly the child's permanent molars. Sealing can begin at about 6 years of age and a painkiller is not needed. The dentist can apply a sealant to each tooth in about five minutes. This bonding lasts for several years and is very cost-effective.</p><br />


<p style="font-size:17px;">Fluoride is another preventive measure to ensure healthy teeth. Fluoride is a chemical that helps in the formation of developing teeth and can even stop early dental decay.</p><br />


<p style="font-size:17px;">Fluoride is found in water (added by water departments), certain foods, as a supplement in some vitamins and in toothpaste. Fluoride also is applied by the dentist. It is important to obtain fluoride from drinking water and foods and from topical treatments of fluoride, such as toothpaste and dental treatments.</p><br />

<p style="font-size:17px">Be sure to check the fluoride level in the water where you live. Families drinking only bottled water should contact the bottling company to determine the amount of fluoride in the water. If your family uses well water, have the fluoride content tested by the state Hygiene Lab. If your water is supplied by your municipality, call your city water department. (Most city water systems are adequately fluoridated.) A range of .7 to 1 part of fluoride per million is the best level.</p><br />


<p style="font-size:17px;">If fluoride levels are low in your drinking water, a dentist or pediatrician can prescribe chewable tablets or liquid containing fluoride, or supplemental fluoride treatments.</p>


<p style="font-size:17px;"><b>Tips for taking your child to the dentist include:</b></p>


<p style="font-size:17px;">1. Be honest with your child about an upcoming visit to the dentist.</p>
<p style="font-size:17px;">2. If you are worried about going to the dentist, do not talk about your worries to your child. Do not say "Don't worry, it won't hurt," as this associates the dentist with the word "hurt."</li></p>
<p style="font-size:17px;">3. Give the dentist freedom to work with your child. The dentist knows how to help a child feel more at ease in the dental chair.</p>




<p style="font-size:13px;"><b>Remember, when dental visits begin at an early age, a child learns not to fear dental exams.</b></p>

