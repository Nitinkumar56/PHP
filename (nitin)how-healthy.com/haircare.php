  <h2>Prevent hair loss, hair care</h2>

<p style="font-size:17px;">Most people think about preventing hair loss only after they have lost a lot of it. But if you start early (like when you still have a head full of hair) it works out a lot easier both emotionally and financially. In fact, the truth is - preventing hair loss is a simpler process than treating hair loss. Plainly put, it all boils down to some sensible hair care - if you take care to keep the hair you’ve got, you will be blessed with better looks and healthier hair. So here are a few things you can do to prevent your hair from falling:</p><br />
  
 <p style="font-size:17px;">1. Follow a healthy lifestyle,</p>
 
 <p style="font-size:17px;">2. Take care of your hair,</p>

 <p style="font-size:17px;">3. Avoid things bad for your hair, and</p>
 
 <p style="font-size:17px;">4. Adopt simple Home treatments.</p><br />


  
<p style="font-size:17px;"><b>1. Follow a Healthy Lifestyle</b></p>


 <p style="font-size:17px;">Hair, like your skin and your nail, is often a barometer of general health. If you are healthy, your hair will probably look good. And if your hair is falling, it could be a sign of possible health problems.</p><br />


<p style="font-size:17px;">So everything you do for good health also applies to hair: eat your fruits and vegetables (Vitamin B, Iron, Calcium and Zinc have especially been linked to healthy hair); always consume a protein-rich diet (hair is made of proteins), and drink adequate water.</p><br />

 

 <p style="font-size:17px;"><b>2. Take care of your hair</b></p>

<p style="font-size:17px;">Dirty hair is more likely to get infected and more likely to fall – so keep your hair clean. Buy good-quality shampoo and hair products (in the long run this is cheaper than treating hair that is already half gone). When you shampoo your hair, concentrate on the scalp and hair roots. When applying a conditioner or hair lotion, it is better to concentrate on the ends of your hair (which tend to dry out faster and are more vulnerable to the damage that comes from exposure).</p>

 <p style="font-size:17px;">Trimming you mane regularly helps your hair look and feel better. Besides, it’s also a long “bye-bye” to those annoying split ends!</p><br />



<p style="font-size:17px;"><b>3. Avoid things that are bad for your hair</b></p>

   
  <p style="font-size:17px;">This seems so obvious it should not need telling. But it apparently does need to be told again because many of us unknowingly adopt several unhealthy hair-practices:</p>
 
  <nav style="font-size:17px;">
  <p>Wearing styles that pull the hair tight (usually into braids or pony tails). Many women pull their hair so tight it actually comes out of its roots. If you change your hairstyle soon enough, your hair will recover. Otherwise, your hair might permanently stop growing from the areas it is pulled most from.</p>
  
<p>Any chemical treatments (even so-called ‘herbal’ ones) designed to drastically change the way your hair looks (styling, perming, straightening, hot-oil treatments, hot ironing).</p>
<p>Blow-drying your hair – this dries out the scalp and damages hair follicles. Allow your hair to air dry at least part of the time, or change the heat setting on your blower to the lowest.</p>
<p>Combing your hair or rough toweling when its wet – this will put out some hair strands from the roots and stretch and break others</p></nav>


<p  style="font-size:17px;">Even the habit of pulling out hair might do more harm than you realize (some people actually go bald from this).</p>


<p style="font-size:17px;"><b>The idea is to be gentle with your hair. Do nothing that might harm your hair in any way.</b></p><br />



<p style="font-size:17px;"><b>4. Home treatments for healthy hair</b></p>

  
  <p style="font-size:17px">Here we’ve mentioned a few home treatments that generations of Indian women have relied on. Give these ancient secrets a shot yourselves!</p>

<nav style="font-size:17px">

 <p>Make a paste of henna, curd, soaked and ground fenugreek, ground hibiscus flowers, ground gooseberries, and egg (all of them or any one) and apply it onto the hair and scalp for about an hour before shampooing. Done regularly, this will make your hair stronger and better-looking. But remember that henna is suitable only for dark colored hair because it tints hair red.</p>
<p>Wash the hair out with decoction of tea or a bit of lemon squeezed into the water in which you will bathe. (for dandruff).</p>
<p>Apply coconut or almond oil. If doing this daily does not appeal to you, apply the oil on the scalp overnight before shampooing, or apply a drop to the top of your head before you comb out your hair.</p>

<p style="font-size:17px;">Never ever take your hair for granted. The adage “Prevention is better than cure” fully applies to your hair too. Remember, you will sorely miss those beautiful tresses if you let them go away!</p>



