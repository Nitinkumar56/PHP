 <h2>Migraine headache</h2>

<p style="font-size:17px; color:#990000"><b>Introduction:</b></p>
 
<p style="font-size:17px;">Migraines are extremely painful, recurring headaches that are sometimes accompanied by other symptoms such as visual disturbances -- seeing an aura -- or nausea. There are two types of migraine -- migraine with aura, formerly called common migraines, and migraine without aura, formerly called classic migraines.</p>

<p style="font-size:17px;">If you have a migraine with aura, you may see things such as stars or zigzag lines or have a temporary blind spot about 30 minutes before the headache starts. Even if you don't experience an aura, you may have other warning signs in the period before the headaches starts, such as a craving for sweets, thirst, sleepiness, or depression.</p>

<p style="font-size:17px;">Although there is no cure for migraines, you can manage the condition by reducing the frequency of attacks and reducing pain once an attack starts.</p>

<p style="font-size:17px; color:#990000"><b>Signs and Symptoms:</b></p>

<p style="font-size:17px;">The headache from a migraine, with or without aura, has the following characteristics:</p>

<nav style="font-size:17px;">
<p>Throbbing, pounding, or pulsating pain</p>
<p>Often begins on one side of your head and may spread to both or stay on one side</p>
<p>Most intense pain is often concentrated around the sides of the forehead</p>
<p>Can last from 4 - 72 hours</p>
</nav>

<p style="font-size:17px;"><b>These symptoms may happen at the same time or before the headache:</b></p>

<nav style="font-size:17px;">
<p>Nausea and vomiting</p>
<p>Dizziness, lightheadedness, or even vertigo (feeling like the room is spinning)</p>
<p>Loss of appetite</p>
<p>Fatigue</p>
<p>Visual disturbances, like seeing flashing lights or zigzag lines, temporary blind spots, or blurred vision</p>
<p>Parts of your body may feel numb, weak, or tingly</p>
<p>Light, noise, and movement -- especially bending over -- make your head hurt worse; you want to lie down in a dark, quiet room</p>
<p>Irritability</p>
</nav>   

<p style="font-size:17px;"><b>Symptoms that may linger even after the headache is gone</b></p>


<nav style="font-size:17px;">
<p>Feeling mentally dull, like your thinking is not clear or sharp</p>
<p>Sleepiness</p>
<p>Neck pain</p>
</nav>

<p style="font-size:17px; color:#990000"><b>Treatment Approach:</b></p>

<p style="font-size:17px;">Treatment for migraines is aimed at preventing them from happening and reducing pain once an attack starts.</p>

 
 <p style="font-size:17px;">You can control your migraines with a combination of medications, lifestyle changes, and complementary therapies. Biofeedback (see also: Mind-body medicine) may help you control the initial contraction of blood vessels. Relaxation techniques may reduce both the frequency and intensity of attacks.</p>


 <p style="font-size:17px;"><b>1.Lifestyle</b></p>


<p style="font-size:17px;">Keeping a migraine diary, particularly when you first begin to have migraines, can help identify the triggers for your headaches so you can avoid them.</p>

<nav style="font-size:17px;">
<p>Avoiding cigarettes, caffeine, and alcohol</p>
<p>Exercising regularly</p>
<p>Getting enough sleep each night</p>
<p>Relaxing and reducing stress in your life (see Mind-Body Medicine section)</p>
<p>Eating regular meals</p>
</nav>


<p style="font-size:17px;"><b>2.Medications</b></p>

<p style="font-size:17px">Medications for migraines can be classified in two major categories: those designed to prevent attacks, and those designed to relieve pain.</p>

 <p style="font-size:17px"><b>3.Massage and Physical Therapy</b></p>

<p style="font-size:17px;">Reflexology is a technique that places pressure on specific "reflex points" on the hands and feet that are believed to correspond to areas throughout the body. Some early studies suggest it may relieve pain and allow people with migraines to take less pain medication. However, more research is needed. Practitioners believe reflexology helps you become more aware of your own body signals, which might help you sense the signals that a migraine is about to occur -- before pain starts. They also believe reflexology helps improve general well-being and energy level.</p>

<p style="font-size:17px;"><b>4.Acupuncture</b></p>

<p style="font-size:17px;">Acupuncture has been studied as a treatment for migraine headache for more than 20 years. While not all studies have shown that it helps, researchers agree that acupuncture appears safe and that it may work for some people. A study published in 2003 suggest that getting an acupuncture treatment when migraine symptoms first start works as well as taking the drug Imitrex. As symptoms continue, however, the medication works better than acupuncture.</p>


<p style="font-size:17px;">In addition to needling treatment, acupuncturists may recommend lifestyle changes, such as suggestions for specific breathing techniques, qi gong exercise, and changes to your diet.</p>


