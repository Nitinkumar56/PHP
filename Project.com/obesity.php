    <h2>Obesity</h2>
    <p style="font-size:17px;">Obesity is a growing global health problem. Obesity is when someone is so overweight that it is a threat to their health.</p>
    <br />
    <p style="font-size:17px;">Obesity typically results from over-eating (especially an unhealthy diet) and lack of enough exercise</p>
    <br />
    <p style="font-size:17px;">In our modern world with increasingly cheap, high calorie food (example, fast food — or “junk food”), prepared foods that are high in things like salt, sugars or fat, combined with our increasingly sedentary lifestyles, increasing urbanization and changing modes of transportation, it is no wonder that obesity has rapidly increased in the last few decades, around the world.</p>
    <br />
    <h2>What Causes Overweight and Obesity?</h2>
    <p style="font-size:17px;"><b>1.Lack of Energy Balance:</b></p>
    <p style="font-size:17px;">A lack of energy balance most often causes overweight and obesity. Energy balance means that your energy IN equals your energy OUT.</p>
    <br />
    <p style="font-size:17px;">Energy IN is the amount of energy or calories you get from food and drinks. Energy OUT is the amount of energy your body uses for things like breathing, digesting, and being physically active.</p>
    <br />
    <p style="font-size:17px;">To maintain a healthy weight, your energy IN and OUT don't have to balance exactly every day. It's the balance over time that helps you maintain a healthy weight.</p>
    <br />
    <p style="font-size:17px;"> The same amount of energy IN and energy OUT over time = weight stays the same</p>
    <p style="font-size:17px;"> More energy IN than energy OUT over time = weight gain</p>
    <p style="font-size:17px;"> More energy OUT than energy IN over time = weight loss</p>
    <p style="font-size:17px;">Overweight and obesity happen over time when you take in more calories than you use.</p>
    <br />
    <p style="font-size:17px;"><b>2.An Inactive Lifestyle:</b></p>
    <p style="font-size:17px;">Many Americans aren't very physically active. One reason for this is that many people spend hours in front of TVs and computers doing work, schoolwork, and leisure activities. In fact, more than 2 hours a day of regular TV viewing time has been linked to overweight and obesity.</p>
    <br />
    <p style="font-size:17px;">Other reasons for not being active include: relying on cars instead of walking, fewer physical demands at work or at home because of modern technology and conveniences, and lack of physical education classes in schools.</p>
    <br />
    <p style="font-size:17px;">People who are inactive are more likely to gain weight because they don't burn the calories that they take in from food and drinks. An inactive lifestyle also raises your risk for coronary heart disease, high blood pressure, diabetes, colon cancer, and other health problems.</p>
    <br />
    <p style="font-size:17px"><b>3.Medicines:</b></p>
    <p  style="font-size:17px">Certain medicines may cause you to gain weight. These medicines include some corticosteroids, antidepressants, and seizure medicines.</p>
    <br />
    <p style="font-size:17px;">These medicines can slow the rate at which your body burns calories, increase your appetite, or cause your body to hold on to extra water. All of these factors can lead to weight gain.</p>
    <br />
    <p style="font-size:17px;"><b>4.Smoking:</b></p>
    <p style="font-size:17px;">Some people gain weight when they stop smoking. One reason is that food often tastes and smells better after quitting smoking.</p>
    <br />
    <p style="font-size:17px;">Another reason is because nicotine raises the rate at which your body burns calories, so you burn fewer calories when you stop smoking. However, smoking is a serious health risk, and quitting is more important than possible weight gain.</p>
    <br />
    <p style="font-size:17px;"><b>5.Lack of Sleep:</b></p>
    <p style="font-size:17px">Research shows that lack of sleep increases the risk of obesity. For example, one study of teenagers showed that with each hour of sleep lost, the odds of becoming obese went up. Lack of sleep increases the risk of obesity in other age groups as well.</p>
    <br />
    <p style="font-size:17px;">People who sleep fewer hours also seem to prefer eating foods that are higher in calories and carbohydrates, which can lead to overeating, weight gain, and obesity.</p>
    <br />
    <p style="font-size:17px;">Sleep helps maintain a healthy balance of the hormones that make you feel hungry (ghrelin) or full (leptin). When you don't get enough sleep, your level of ghrelin goes up and your level of leptin goes down. This makes you feel hungrier than when you're well-rested.</p>
    <br />
    <p style="font-size:17px;">Sleep also affects how your body reacts to insulin, the hormone that controls your blood glucose (sugar) level. Lack of sleep results in a higher than normal blood sugar level, which may increase your risk for diabetes.</p>
    <br />
    <p style="font-size:17px;"><b>6.Emotional Factors:</b></p>
    <p style="font-size:17px">Some people eat more than usual when they're bored, angry, or stressed. Over time, overeating will lead to weight gain and may cause overweight or obesity. </p>
    <br />
    <p style="font-size:17px;"><b>7.Genes and Family History:</b></p>
    <p style="font-size:17px">Studies of identical twins who have been raised apart show that genes have a strong influence on a person's weight. Overweight and obesity tend to run in families. Your chances of being overweight are greater if one or both of your parents are overweight or obese.</p>
    <br />
    <p style="font-size:17px;">Your genes also may affect the amount of fat you store in your body and where on your body you carry the extra fat. Because families also share food and physical activity habits, a link exists between genes and the environment.</p>
    <br />
    <p style="font-size:17px;">Children adopt the habits of their parents. A child who has overweight parents who eat high-calorie foods and are inactive will likely become overweight too. However, if the family adopts healthy food and physical activity habits, the child's chance of being overweight or obese is reduced. </p>
