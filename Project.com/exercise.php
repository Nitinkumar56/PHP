   <p><li> Anaerobic exercises, such as weight training, functional training, eccentric training or sprinting and high-intensity interval training, increase short-term muscle strength.</li></p></nav><br />

   
   <h2>Top Five Exercises That You Can Do At Your Home:</h2>
   <hr />

  <p  style="font-size:17px"><b>1.Push ups:</b></p><br />

  <p  style="font-size:17px">This is my all time favourite exercise to do in the house and can be done almost anywhere. If done correctly it can strengthen stamina and build your Pecs and triceps.</p><br />


<p style="font-size:17px"><b>How to do a standard push up:</b></p><br />


<p style="font-size:17px">Lie face down with your palms on the floor, about shoulder width apart. Push your whole body up with your back and legs in a straight line and then lower yourself back down towards the floor. To strengthen your chest more place your arms wider apart. To strengthen your triceps and back more, move your arms closer together. Muscles used: Chest muscles (pectoral) Triceps (backs of the upper arm) As a guide I have shown some stats for pushups from the armed forces.</p><br />


<p style="font-size:17px"><b>2.Sit ups</b></p><br />



<p style="font-size:17px">This is another great exercise to do in the house and aging can be done almost anywhere. There are lots of ways to do sit ups. I am using the standard sit up. How to do a sit up: Lie on your back with your knees bens and feet flat on the floor. Cross your arms across your chest and raise your body up to try and meet your knees. This can cause strain on the back, so if you have a weak back it may be better to do a crunch instead. Muscles used in sit ups: Abdominal muscles Back muscles.</p><br />
   

	
<p style="font-size:17px"><b>3.Squats</b></p><br />


<p style="font-size:17px">Another easy but very useful exercise for your thighs and buttocks. It helps to shape your buttocks, strengthen thighs and build stamina. How to do it: Stand up straight, bend your knees with your arms stretched outright parallel to the floor. Bend until your thighs are parallel to the floor. Muscles used; Thigh muscles Buttock muscles.</p><br />


 
<p style="font-size:17px"><b>4.Jumping on the spot</b></p><br />

<p style="font-size:17px">This might look a bit silly but it’s a great workout and is used to get the heart pumping and build stamina. It can put strain on the knees. This will also build your leg muscles. How to do it; Literally just jump up and down raising your whole body off the ground 1 inch or more. Muscles used: Calf muscles Thigh muscles.</p> <br />



<p style="font-size:17px"><b>5.Back extension</b></p><br />


<p style="font-size:17px">I have had back problems for years especially when I get up first thing in the morning. I bought a back book and tried all the exercises in it for 4 weeks and I found it made a big difference. I could stay in bed longer, if I wanted without getting so much pain. One of the best exercises was the back extension. How to do it: Lie face down with your arms at your side. Slowly lift your head toward the ceiling and look up as far as you can go. You can extend this by lifting your legs and your head up as far as you can and hold for 5 and release. Muscles used: Stomach muscles Back muscles What are your favourite exercies to do in the home? let me know by leaving a comment.</p>
