<h2>10 Habits of a Healthy Family Culture</h2>

<p>For many, living a rich life includes a rich family life that involves connection, sharing, closeness,
     and love. Yet achieving that is much easier said than done. How can we intentionally create a healthy 
     family culture? What are some of its characteristics?</p>

<h2>I offer the following for consideration:</h2>

<h2>1. Establish shared family values.</h2>
<p>In successful families, founding generations do more than expect younger members to live up to the family values.  They find ways to consciously teach those values as well as modeling them in their own behavior.  Families who flourish also understand that there are many ways to demonstrate values such as stewardship, hard work, and excellence.  Such families are secure and flexible enough to accommodate varying lifestyles and behaviors because they focus on the values that underlie those surface appearances.</p>

<h2>2. Define a family mission and vision.</h2>
<p>There is no one “right” or “best” value to include in a family mission statement.  It’s common for these statements to include commitments like the following: to support family members in their individual growth, to use the wealth for the good of their community, to support specific causes such as education or medical research, to increase the family wealth, and to foster family closeness.</p>

<h2>3. Establish healthy limits or boundaries.</h2>
<p>Flourishing families don’t pretend their wealth doesn’t exist or refuse to use it, but they choose to live relatively ordinary lives.  Adults practice the value of restraint by setting reasonable limits and boundaries for themselves in both financial and non-financial ways.  They set similar boundaries for their children in order to actively teach them responsibility and restraint.</p>

<h2>4. Support family members in leading lives with purpose.</h2>
<p>One of the greatest fears for many wealth creators is that their success will enable future generations to become dependent and live off the family money without becoming contributing members of society.  Successful legacy families are very intentional in supporting each member of the family to live a life with purpose and meaning. The family isn’t threatened by differences in personality, thought or temperament.  Instead, it values the differences and sees them as contributing to the strength of the family.</p>

<h2>5. Prepare heirs to manage wealth in ways to foster well-being.</h2>
<p>Families that flourish do active financial parenting with both young and adult children. When children are small, parents teach them the basics of money skills: setting aside money for saving, spending, and sharing.  As the children approach their teen years, the parents begin teaching them how to earn money and keep a budget.  They allow and encourage adult children to grow up, and they seek out advisors to teach them about wealth management.  Most importantly, members of older generations model responsible stewardship by educating themselves financially and being actively involved in managing the family money.</p>

<h2>6. Practice Skillful Communication.</h2>
<p>What families who communicate well have in common is an intention to be clear and open.  They avoid the manipulation, secrets, and power games that foster dysfunction and distrust. Instead they have a commitment to transparency.  They are willing to learn from each other, listen to one another, and create an atmosphere of tolerance for differences.  They are willing to work together to resolve conflicts, with help from the professionals as necessary.</p>

<h2>7. See the family as a learning system.</h2>
<p>Families who are most successful at passing their values on to future generations understand the importance of seeing mistakes as something to learn from.  They are willing to learn from advisors and confident enough to seek out those who are willing to “speak truth to power.”  They cultivate a sense of appropriate humility.</p>

<h2>8. See the family as a steward of the wealth.</h2>
<p>Most of the legacy families that I have worked with or interviewed discuss openly the family’s responsibility to be the steward of the wealth.  There is a deep sense that “to whom much is given, much is expected.”  They describe themselves as the custodians of their wealth and of the well-being of others. I have worked with a number of families who had very different political or social beliefs from each other, but they were in alignment around the value of making a contribution in the lives of others.</p>

<h2>9. Value giving back.</h2>
<p>A sense of gratitude and a desire to give back are common in flourishing legacy families.  Many of them set up charitable foundations to manage their giving.  Others work through their churches, community organizations, or existing charitable organizations.  In addition, many family members give generously of their time and expertise as well as their money.</p>

<h2>10. Have a long-term view of the family.</h2>
<p>It’s important for all adult members of the family to talk about the future, as this kind of thinking dramatically resets conversations.  They emphasize questions like, “What’s your dream? What do you want to be looking back on 35 years from now? In the afterlife, whatever your concept of that may be, what do you want to be looking down and seeing in this family?  The focus shifts away from seeing the family money as an ATM for the purpose of allowing family members to withdraw whatever they want whenever they want it.  Instead, the money becomes a shared asset and long-term resource to be tended and used responsibly.</p>


